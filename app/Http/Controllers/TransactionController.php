     */
    public function index()
    {
        $user = Auth::user();

        $ledgers = $user->ledgers;

        $transactions = RawTransaction::whereIn('account_id', $ledgers)->orderBy('date', 'desc')->get();

        return view(
            'transactions.index',
            [
                'user' => $user,
                'transactions' => $transactions
            ]
        );
    }

